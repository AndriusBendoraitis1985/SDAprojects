package lt.sdaacademy.fundamentals.loop.forEach.Example1;

class Animal {
    // kintamieji
    private String name;
    private String breed;


    // konstruktoriai
    Animal(String n, String b) {
        this.name = n;
        this.breed = b;

  }

    // kintamojo getteris
    public String getName() {
        return name;
    }
    public String getBreed() {
        return breed;
    }

}
